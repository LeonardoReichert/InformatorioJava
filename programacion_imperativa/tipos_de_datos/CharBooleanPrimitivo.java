package programacion_imperativa.tipos_de_datos;

public class CharBooleanPrimitivo{
    public static void main(String[] args){
        
        System.out.println("datos de tipo char");

        /* los char se escriben con comillas simples */

        char letraF = 'f';
        char espacio = '\u005f';

        System.out.println("letraF vale: "+letraF);
        System.out.println("espacio vale: "+espacio);

        boolean valorBooleano = true;
        boolean valorBooleano2 = false;

        System.out.println("booleano1 vale "+valorBooleano);
        System.out.println("booleano2 vale "+valorBooleano2);

    }
}

